    private String Nom(int id)
    {
        
        for(int i=0;i<projetjava.getAcheteurs().size();i++)
        {
            if(projetjava.getAcheteurs().get(i).getId()==id)
            {
                 nameU = projetjava.getAcheteurs().get(i).getNom();
            }
        }
        return nameU;
    }
    
    private String Prénom(int id)
    {
        
        for(int i=0;i<projetjava.getAcheteurs().size();i++)
        {
            if(projetjava.getAcheteurs().get(i).getId()==id)
            {
                 prenom = projetjava.getAcheteurs().get(i).getPrenom();
            }
        }
        return prenom;
    }
