    private void PreviousOrdersActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PreviousOrdersActionPerformed
        // AFFICHAGE DES ANCIENNES COMMANDES DE L'UTILISATEUR DANS LA JTABLE
        
        for (int i = 0; i < projetjava.getCommandes().size(); i++) {
            if (projetjava.getCommandes().get(i).getAcheteur() == IdAcheteur) {
                String num_order = String.valueOf(projetjava.getCommandes().get(i).getNumCmd());
                double prix = 0;
                for (int j = 0; j < projetjava.getCommandes().get(i).getPrixProduits().size(); j++) {
                    prix = prix + projetjava.getCommandes().get(i).getPrixProduits().get(j);
                    //System.out.println(projetjava.getCommandes().get(i).getPrixProduits().get(j));

                }
                String price = String.valueOf(prix);
                //System.out.println(price);
                String Table1[] = {num_order, price + " €"};
                DefaultTableModel Tblmodel = (DefaultTableModel) jTable1.getModel();
                Tblmodel.addRow(Table1);
            }
        }
        jPanel1.setVisible(true);
    }//GEN-LAST:event_PreviousOrdersActionPerformed

