    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
        id = idAlea();
        boolean test = false;

        if (LastName.getText().equals("") || FirstName.getText().equals("") || Mail.getText().equals("") || Password.getText().equals("") || ConfirmPassword.getText().equals("") || !Password.getText().equals(ConfirmPassword.getText())) {
            test = true;
            System.out.println("Erreur membre");
        }
        if (!test) {
            Vendeur newVendeur = new Vendeur(id, Password.getText(), Mail.getText(), LastName.getText(), FirstName.getText());
            projet.addVendeurs(newVendeur);
            EmployeeFrame emp = new EmployeeFrame(loginvendeursframe, welcomeFrame, projet, id);
            welcomeFrame.fullScreen(emp, this);
        }
    }//GEN-LAST:event_SaveActionPerformed

