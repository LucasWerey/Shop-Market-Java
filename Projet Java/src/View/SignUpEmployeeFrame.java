    public int idAlea() {

        Random r = new Random();
        int n = r.nextInt(1000);

        for (int i = 0; i < projet.getVendeurs().size(); i++) {
            if (n == projet.getVendeurs().get(i).getId()) {
                idAlea();
                //  System.out.println("erreur");
                //  System.out.println(n);
            }

            //  System.out.println(n); 
        }
        return n;
    }

